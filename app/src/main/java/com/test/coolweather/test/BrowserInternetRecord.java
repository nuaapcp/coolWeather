package com.test.coolweather.test;

public class BrowserInternetRecord {
    private String url;
    private String visits;
    private String date;
    private String bookmark;
    private String title;
    private String created;
    private String favicon;
    private String thumbnail;
    private String touch_icon;
    private String user_entered;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getVisits() {
        return visits;
    }

    public void setVisits(String visits) {
        this.visits = visits;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getBookmark() {
        return bookmark;
    }

    public void setBookmark(String bookmark) {
        this.bookmark = bookmark;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getCreated() {
        return created;
    }

    public void setCreated(String created) {
        this.created = created;
    }

    public String getFavicon() {
        return favicon;
    }

    public void setFavicon(String favicon) {
        this.favicon = favicon;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public void setThumbnail(String thumbnail) {
        this.thumbnail = thumbnail;
    }

    public String getTouch_icon() {
        return touch_icon;
    }

    public void setTouch_icon(String touch_icon) {
        this.touch_icon = touch_icon;
    }

    public String getUser_entered() {
        return user_entered;
    }

    public void setUser_entered(String user_entered) {
        this.user_entered = user_entered;
    }
}
